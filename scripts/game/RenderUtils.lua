-- ============================================================================
-- 辅助：NanoVG 颜色创建
-- ============================================================================
function RenderUtils.rgba(c, alphaOverride)
    local a = alphaOverride or c[4] or 255
    return nvgRGBA(c[1], c[2], c[3], a)
end

-- ============================================================================
-- 世界坐标 → 屏幕坐标
-- ============================================================================
function RenderUtils.worldToScreen(wx, wy, cam, sw, sh)
    return (wx - cam.x) + sw * 0.5, (wy - cam.y) + sh * 0.5
end

return RenderUtils
